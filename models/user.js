var mongoose = require('mongoose');
var Schema = mongoose.Schema;
// define model =========================================================================
var UserSchema = new Schema({
    username: String,
    password: String,
    name: String,
    age: Number,
    gender: String,
    address: String,
    phone: String,
    homephone: String,
    dob: Date,
    nic: String,
    picture: String,
    role: {
        isTeacher: Boolean,
        isClassTeacher: Boolean,
        isHeadOfDepartment: Boolean,
        isAdmin: Boolean,
        isStudent: Boolean,
    },
    isActive: Boolean,
    dateCreated: Date
});

module.exports = mongoose.model('User', UserSchema);